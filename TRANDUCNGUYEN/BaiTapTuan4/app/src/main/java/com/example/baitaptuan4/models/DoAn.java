package com.example.baitaptuan4.models;

public class DoAn {
    int ivDoan;
    String name;

    public DoAn(int ivDoan, String name) {
        this.ivDoan = ivDoan;
        this.name = name;
    }

    public int getIvDoan() {
        return ivDoan;
    }

    public void setIvDoan(int ivDoan) {
        this.ivDoan = ivDoan;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
